#include "3-calc.h"
#include <stdio.h>
/**
 * op_add - addition operator
 * @a: first
 * @b: second
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtraction operator
 * @a: first
 * @b: second
 * Return: a + b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication operator
 * @a: first
 * @b: second
 * Return: a + b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division operator
 * @a: first arg
 * @b: second arg
 * Return: a + b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - module operator
 * @a: first arg
 * @b: second arg
 * Return: a + b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
